import turtle as t
t.shape("turtle")
t.color('black')
t.penup()
t.goto(100, 0)
t.left(90)
t.pendown()
t.fillcolor("yellow")
t.begin_fill()
t.circle(100)
t.end_fill()
t.penup()
t.goto(-30, 50)
t.pendown()
t.fillcolor('blue')
t.begin_fill()
t.circle(15)
t.end_fill()
t.penup()
t.goto(60, 50)
t.pendown()
t.fillcolor('blue')
t.begin_fill()
t.circle(15)
t.end_fill()
t.penup()
t.goto(0, 30)
t.pendown()
t.color('black')
t.pensize(6)
t.left(180)
t.forward(35)
t.penup()
t.goto(50, -10)
t.color('red')
t.pendown()
t.circle(-50, 180)
t.exitonclick()
